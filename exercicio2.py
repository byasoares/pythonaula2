numero = int (input("Digite um número inteiro e positivo: "))

def extenso (numero):
    if numero == 1:
        print("Número Um.")

    elif numero == 2:
        print("Número Dois.")

    elif numero == 3:
        print("Número Três.")

    elif numero == 4:
        print("Número Quatro.")

    elif numero == 5:
        print("Número Cinco")

    elif numero:
        print("Número Inválido")

extenso(numero)
